package src.Basics.conditionalStatements;

public class SwitchStatement {
    public static void main(String[] args) {
        int a = 47;
        switch (a) {
            case 1:
                System.out.println("a as the value of 1");
                break;
            case 2:
                System.out.println("a as the value of 2");
                break;
            case 3:
                System.out.println("a as the value of 3");
                break;
            case 4:
                System.out.println("a as the value of 4");
                break;
            case 5:
                System.out.println("a as the value of 5");
                break;
            case 6:
                System.out.println("a as the value of 6");
                break;
            case 7:
                System.out.println("a as the value of 7");
                // break;
            case 8:
                System.out.println("a as the value of 8");
                // break;
            case 9:
                System.out.println("a as the value of 9");
                // break;
            case 10:
                System.out.println("a as the value of 10");
                break;

            default:
            System.out.println("nothing to print");
                break;
        }
    }
}
